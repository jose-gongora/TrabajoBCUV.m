% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
fx=get(handles.edit1,'String')
caso=get(handles.text14,'String')%tomamos los datos ingresados en el cajon de texto correspondiente al caso a seleccionar
syms x
switch cell2mat(caso)
    case'Determinar la primera la primera derivada de f'
        fx2=diff(fx,x);
        f2= inline(fx2);
    case'Determinar la segunda la primera derivada de f'
        fx2=diff(fx,x,2);
        f2= inline(fx2);
    case'Determinar la tercera la primera derivada de f'
        fx2=diff(fx,x,3);
        f2= inline(fx2);
    case'Determinar la cuarta la primera derivada de f'
        fx2=diff(fx,x,4);
        f2= inline(fx2);
    case'Determinar la quinta la primera derivada de f'
        fx2=diff(fx,x,5);
        f2= inline(fx2);
    case'Determinar la derivada de f con respecto a  x'
        fx2=diff(fx,x);
        f2= inline(fx2);
    case'Determinar la derivada de f con respecto a  y'
        fx2=diff(fx,x);
        f2= inline(fx2);
end
set(handles.edit4,'String',char(fx2))

